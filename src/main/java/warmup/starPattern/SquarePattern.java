package warmup.starPattern;

public class SquarePattern {
    public static void main(String[] args) {
        // number of rows
        for(int i = 0 ; i<4 ; i++){
            // number of columns
            for(int j = 0 ; j<4 ; j++){
                System.out.print("* ");
            }
            System.out.println();
        }
    }
}
